package model;

public class FoodNutrients {
    public final float calorie;
    public final float protein;
    public final float carbs;
    public final float fat;
    
    public FoodNutrients(float cal, float protein, float carbs, float fat) {
        this.calorie = cal;
        this.protein = protein;
        this.carbs = carbs;
        this.fat = fat;
    }
}
